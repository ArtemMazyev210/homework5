# Создание случайного массива из 10 чисел
arr <- sample(-10:10, 10, replace = TRUE)

# Нахождение индекса последнего отрицательного элемента
last_neg_idx <- 0
for (i in 1:length(arr)) {
  if (arr[i] < 0) {
    last_neg_idx <- i
  }
}

# Нахождение суммы элементов после последнего отрицательного элемента
sum_val <- 0
for (i in (last_neg_idx + 1):length(arr)) {
  if (arr[i] > 0) {
    sum_val <- sum_val + arr[i]
  }
}


